class Person{
    constructor(name, age){
        this.name = name;
        this.age = age;
    }
}

const person1 = new Person("Charvi", 22);
const person2 = new Person("Prakarsh", 22);
const person3 = new Person("Neha", 22);
const person4 = new Person("Souji", 22);

console.log(person1.name, person1.age);
console.log(person2.name, person2.age);
console.log(person3.name, person3.age);
console.log(person4.name, person4.age);